package pdfGenerator.bean;

public class PaymentClaim {

    public String pc;
    public String month;
    public String recoverySchedule;
    public String recoveryAmount;
    public String cumulative;

    public String getPc() {
        return pc;
    }

    public void setPc(String pc) {
        this.pc = pc;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getRecoverySchedule() {
        return recoverySchedule;
    }

    public void setRecoverySchedule(String recoverySchedule) {
        this.recoverySchedule = recoverySchedule;
    }

    public String getRecoveryAmount() {
        return recoveryAmount;
    }

    public void setRecoveryAmount(String recoveryAmount) {
        this.recoveryAmount = recoveryAmount;
    }

    public String getCumulative() {
        return cumulative;
    }

    public void setCumulative(String cumulative) {
        this.cumulative = cumulative;
    }




}
