package pdfGenerator.bean;

public class PaymentSummary {

    public String pc;
    public String month;
    public String certification;
    public String advacnePayment;
    public String backcharge;
    public String cumulative;


    public String getPc() {
        return pc;
    }

    public void setPc(String pc) {
        this.pc = pc;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getCertification() {
        return certification;
    }

    public void setCertification(String certification) {
        this.certification = certification;
    }

    public String getAdvacnePayment() {
        return advacnePayment;
    }

    public void setAdvacnePayment(String advacnePayment) {
        this.advacnePayment = advacnePayment;
    }

    public String getBackcharge() {
        return backcharge;
    }

    public void setBackcharge(String backcharge) {
        this.backcharge = backcharge;
    }

    public String getCumulative() {
        return cumulative;
    }

    public void setCumulative(String cumulative) {
        this.cumulative = cumulative;
    }



}
